package com.training.javahackathon;

public class StringConcatenation {
	
	public static void main(String[] args) {
	//primitive data types
	int a = 10;
	double d = 10.33;
	char c = 'c';
	boolean t = true;
	boolean f = false;
	//String is a class not datatype
	String s = "Hello";
	String w = "World";
			
//left to right
	System.out.println(s + w + a + d + c);
	
	
			
	}

}
